def validate_curriculum() -> list[str]:
    errors: list[str] = []
    banned = ("补充一个", "说明这个概念", "记录最容易", "以后补充", "TODO", "TBD")
    if len(LESSONS) != 45:
        errors.append(f"expected 45 lessons, got {len(LESSONS)}")
    if len(LABS) != 4:
        errors.append(f"expected 4 labs, got {len(LABS)}")
    for title, lesson in LESSONS.items():
        fields = (
            lesson.intuition,
            lesson.mechanism,
            lesson.worked_example,
            lesson.work_context,
            lesson.practice,
            lesson.evidence,
            lesson.pitfalls,
        )
        combined = "\n".join(fields)
        if any(not field.strip() for field in fields):
            errors.append(f"{title}: empty field")
        if any(word in combined for word in banned):
            errors.append(f"{title}: placeholder phrase")
        if not re.search(r"\d|×|→|\$|shape|形状", combined):
            errors.append(f"{title}: no concrete number, shape, or formula")
        if title in CORE_LESSONS and lesson.lab not in LABS:
            errors.append(f"{title}: core lesson missing valid lab")
        if "论文证据" in lesson.evidence:
            if not re.search(r"第\s*\d+.*页|图\s*\d+|表\s*\d+", lesson.evidence):
                errors.append(f"{title}: paper evidence missing anchor")
        elif "教材性知识（当前四篇论文未直接覆盖）" not in lesson.evidence:
            errors.append(f"{title}: evidence type not explicit")
        if len(lesson.checklist) < 3:
            errors.append(f"{title}: checklist too short")
    return errors
